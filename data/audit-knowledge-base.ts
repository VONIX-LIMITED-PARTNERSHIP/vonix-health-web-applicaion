// Helper function to search AUDIT knowledge base
export function searchAUDITKnowledge(query: string): AUDITKnowledgeEntry | null {
  const lowerQuery = query.toLowerCase()

  for (const entry of auditKnowledgeBase) {
    for (const keyword of entry.keywords) {
      if (lowerQuery.includes(keyword.toLowerCase())) {
        return entry
      }
    }
  }

  return null
}

// Get knowledge by category
export function getAUDITKnowledgeByCategory(category: AUDITKnowledgeEntry["category"]): AUDITKnowledgeEntry[] {
  return auditKnowledgeBase.filter((entry) => entry.category === category)
}

// Get all AUDIT keywords for intent classification
export function getAllAUDITKeywords(): string[] {
  return auditKnowledgeBase.flatMap((entry) => entry.keywords)
}
