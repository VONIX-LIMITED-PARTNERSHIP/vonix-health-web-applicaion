// Helper function to search PHQ knowledge base
export function searchPHQKnowledge(query: string): PHQKnowledgeEntry | null {
  const lowerQuery = query.toLowerCase()

  for (const entry of phqKnowledgeBase) {
    for (const keyword of entry.keywords) {
      if (lowerQuery.includes(keyword.toLowerCase())) {
        return entry
      }
    }
  }

  return null
}

// Get knowledge by category
export function getPHQKnowledgeByCategory(category: PHQKnowledgeEntry["category"]): PHQKnowledgeEntry[] {
  return phqKnowledgeBase.filter((entry) => entry.category === category)
}

// Get all PHQ keywords for intent classification
export function getAllPHQKeywords(): string[] {
  return phqKnowledgeBase.flatMap((entry) => entry.keywords)
}
